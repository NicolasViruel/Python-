# Reemplazar Subcadenas

cadena = 'Hola, mundo!'
print(f'Cadena original: {cadena}')

# Cadena reemplazada por 'a todos'
nueva_cadena = cadena.replace('mundo', 'a todos')
print(f'Cadena reemplazada por a todos: {nueva_cadena}')

# Reemplazamos 'Hola' por 'Adios'

nueva_cadena2 = cadena.replace('Hola', 'Adios')
print(f'Cadena reemplazada por adios: {nueva_cadena2} ')