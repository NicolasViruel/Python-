# Metodos de cadenas

cadena1 = 'Hola Mundo'
print(f'Cadena original: {cadena1}')
mayusculas = cadena1.upper() #Metodo para hacer mayusculas
print(f'Cadena en mayusculas: {mayusculas}')

print(f'Cadena en minusculas: {cadena1.lower()}') # Metodo para minusculas

cadena2 = '  Juan Perez  ' #variable con espacios
print(f'Cadena con espacios: {cadena2}')
print(f'Cadena sin espacios: {cadena2.strip()}') # Strip metodo para quitar espacios
