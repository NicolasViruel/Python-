# Operadores Aritmeticos

a = 10
b = 3

# Suma +
suma = a + b
print(f'El resulta de suma es: {suma}')

# Resta - 
resta = a - b
print(f'El resultado de resta es: {resta}')

# Multiplcacion *
multiplicacion = a * b
print(f'El resultado de multiplicacion es: {multiplicacion}')

# Division /
division = a / b
print(f'El resultado de division es: {division:.3}')

# Division entera //
division_entera = a // b
print(f'El resultado de division entera es: {division_entera}')

# Modulo de una variable
modulo = a % b
print(f'El modulo resultante es: {modulo}')

# Potencia o Potenciacion
potencia = a ** b # 10 eleveado a la 3 o 10x10x10=1000
print(f'El resultado de potencia es: {potencia}')