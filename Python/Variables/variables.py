# Variables en python

# Declaracion e inicializacion de variables

edad = 36
altura = 1.65
pais = "Argentina"

# Acceder a las variables
print("Valores iniciales:")
print('Edad:', edad)
print('Altura:', altura)
print('Pais:', pais)

# Modificar el valor de una variable
edad = 28
altura = 1.70

print("Valores Modificados:")
print('Edad:', edad)
print('Altura:', altura)

# En python el TIPO es dinamico
edad = "treinta"
print("Edad:", edad)

#Si llamamos a una variable antes de definirla nos devuelve Error
