# Ejemplo de tipos de datos

# Concatenacion o suma de valores

numero_1 = '10'
print(f'Numero uno en cadena: {numero_1}')


numero_2 = '20'
print(f'Numero uno en cadena: {numero_2}')

# Concatenamos los resultamos realizando una suma
resultado = numero_1 + numero_2
print(f'Resultado es una concatenacion: {resultado}')

# Convertimos a tipos enteros
numero1_entero = int(numero_1)
numero2_entero = int(numero_2)

suma = numero1_entero + numero2_entero
print(f'Resultado de la suma: {suma}')